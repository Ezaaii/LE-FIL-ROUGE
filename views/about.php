  <h1>ABOUT US</h1>
  <p>Mazy Culture, c'est tout d'abord une salle à louer. Située à Mazy, elle se trouve à 15 km de Namur, à 9 km de Gembloux, à 3 km de la E42. Pour un anniversaire, une réunion, un repas de famille, un barbecue. Pour organiser un spectacle, un concert, une exposition. Notre salle peut accueillir jusquà 120 personnes. De plus, nous offrons un espace extérieur très agréable par beau temps.
     Mazy Culture, c'est ensuite une association qui propose des partenariats pour le développement de projets socio-culturels. Une "résidence" pour créer et construire ces projets.
     Mazy Culture, c'est aussi un organisateur d'événements. Concerts, spectacles de magie, ...
     Mazy Culture, c'est enfin une coopérative de 72 membres venant de tous horizons et toujours ouverte à accueillir de nouveaux membres.</p>
